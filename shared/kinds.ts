
/**
 * Describes the data associated with a single school using the system
 */
export interface Tenant {
	id: string; // Unique identifier for the tenant, generated by backend
	name: string;
	description?: string;
}

export interface Organization {
	id: string; // Unique identifier for the organization, generated by backend
	parentOrganizationID?: string; // Optional reference to a parent organization for hierarchical structures, if not available, this organization is a root organization
	tenantID: string;
	name: string;
	description?: string;
}

export interface AppUser {
	id: string; // Unique identifier for the user, generated by backend
	email: string;
	tenantID: string;
	organizationID: string;
}

export interface AppUserDetail 
	extends Pick<
		AppUser,
		"id" | "email" | "tenantID" | "organizationID"
	> {
	roleIDs: string[];
}

export interface AppUserUpsertPayload {
	email: string;
	organizationID: string;
	roleIDs: string[];
}

export interface Student {
	id: string; // Unique identifier for the student, pregenerated by school, entered manually into system by admin
	tenantID: string;
	firstName: string;
	lastName: string;
}

export interface AppUserSession {
	sessionID: string; // Unique identifier for the session, generated by backend
	tenantID: string;
	userID: string;
	tenantName: string;
	organizationID: string;
	organizationName: string;
}

export interface AppPermission {
	name: string;
	description: string;
}

export const AppPermissions = {
	CREATE_USERS: {
		name: "Create users",
		description: "Add new users"
	},
	DELETE_USERS: {
		name: "Delete users",
		description: "Delete existing users"
	},
	VIEW_USERS: {
		name: "View users",
		description: "View specified users"
	},
	LIST_USERS: {
		name: "List users",
		description: "List users without directly accessing them"
	}
} as const satisfies Record<string, AppPermission>;

export interface AppRole {
	id: string; // UUID
	tenantID: string;
	name: string;
	description: string;
	appPermissions: AppPermission[];
}

export interface UserRole {
	tenantID: string;
	appUserID: string;
	appRoleID: string;
}